    private void jButtonDownloadActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        if (this.wallpaperInfo == null || this.wallpaperInfo.getImagePath() == null) {
            JOptionPane.showMessageDialog(this, "Data gambar tidak valid! tidak ada di database", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        File fileWallpaper = new File("src/uploads/" + this.wallpaperInfo.getImagePath());

        if (!fileWallpaper.exists()) {
            JOptionPane.showMessageDialog(this, """
                    Gagal mendownload! File asli tidak ditemukan di folder: "src/uploads/"
                    Kemungkinan file telah terhapus atau dipindahkan secara manual.""",
                    "File Tidak Ditemukan",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Pilih Lokasi Simpan Wallpaper");

        String imagePath = this.wallpaperInfo.getImagePath();
        String extension = imagePath.substring(imagePath.lastIndexOf("."));

        String namaFileBersih = this.wallpaperInfo.getTitle().replaceAll("[^a-zA-Z0-9]", "_") + extension;
        fileChooser.setSelectedFile(new File(namaFileBersih));

        int pilihanUser = fileChooser.showSaveDialog(this);

        if (pilihanUser == JFileChooser.APPROVE_OPTION) {
            File fileTujuan = fileChooser.getSelectedFile();

            try {
                Files.copy(fileWallpaper.toPath(), fileTujuan.toPath(), StandardCopyOption.REPLACE_EXISTING);
                JOptionPane.showMessageDialog(this, "Wallpaper berhasil diunduh!", "Sukses",
                        JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal mengunduh: " + e.getMessage(), "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        if (this.wallpaperInfo == null) {
            JOptionPane.showMessageDialog(this, "Data wallpaper tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int deleteConfirmation = JOptionPane.showConfirmDialog(this, "Delete Wallpaper " + wallpaperInfo.getTitle(),
                "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (deleteConfirmation != JOptionPane.YES_OPTION) {
            return;
        }

        // delete dari database
        String deleteQuerySQL = "DELETE FROM artworks WHERE id = ?";

        try (Connection con = DBConnection.getConnection();
                PreparedStatement deletePstmt = con.prepareStatement(deleteQuerySQL)) {

            deletePstmt.setInt(1, wallpaperInfo.getId());

            int barisTerdelete = deletePstmt.executeUpdate();

            if (barisTerdelete > 0) {

                // delete dari folder uploads
                File imageFilePath = new File("src/uploads/" + wallpaperInfo.getImagePath());

                if (imageFilePath.exists()) {
                    imageFilePath.delete();
                } else {
                    System.out.println("File fisik tidak ditemukan di folder uploads, hanya membersihkan data database.");
                }

                JOptionPane.showMessageDialog(this, "Wallpaper berhasil dihapus");

                this.setVisible(false);
                this.dispose();

                if (this.mainFrame != null) {
                    this.mainFrame.setVisible(true);
                    if (this.mainFrame instanceof ProfileFrame profileFrame)profileFrame.showGalleryWallpaperUser();
                    else if(this.mainFrame instanceof ManageFrame manageFrame)manageFrame.showWallpaperGalleryAll();
                     
                }

            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus! Data tidak ditemukan di database.", "Peringatan",
                        JOptionPane.WARNING_MESSAGE);
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan database: " + e.getMessage(), "Error",
                    JOptionPane.ERROR_MESSAGE);
        }

    }

